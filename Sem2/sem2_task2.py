#======================================================================================
# Для натурального n создать словарь индекс-значение, состоящий из элементов 
# последовательности 3n + 1.
#======================================================================================

number = int(input('Введите число: '))
list = {} # This is dictionary with keys/values

for i in range(1, number):
    list[i] = i * 3 + 1
print(list)